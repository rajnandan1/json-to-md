// Generates go/slugranges_gen.go from the installed github-slugger package.
// The TS build bundles github-slugger, so its exact strip set is part of the
// byte-identical parity oracle. Probing slug() (maintainCase=true) per code
// point captures the regex's effective behavior without porting the regex.
// Rerun after upgrading github-slugger: node scripts/gen-slug-ranges.mjs
import { writeFile, mkdir } from "node:fs/promises";
import { slug } from "github-slugger";

const stripped = [];
for (let cp = 0; cp <= 0x10ffff; cp++) {
  if (cp >= 0xd800 && cp <= 0xdfff) continue; // lone surrogates never reach the slugger
  const ch = String.fromCodePoint(cp);
  if (cp === 0x20) continue; // space becomes '-', handled in code
  const out = slug(ch, true);
  if (out === ch) continue; // kept
  if (out === "") {
    stripped.push(cp); // removed
    continue;
  }
  throw new Error(`code point ${cp.toString(16)} partially transformed: ${JSON.stringify(out)}`);
}

const ranges = [];
for (const cp of stripped) {
  const last = ranges[ranges.length - 1];
  if (last && cp === last[1] + 1) last[1] = cp;
  else ranges.push([cp, cp]);
}

const lines = ranges.map(([lo, hi]) => `\t{0x${lo.toString(16)}, 0x${hi.toString(16)}},`);
const src = `// Code generated by scripts/gen-slug-ranges.mjs from github-slugger@2.0.0. DO NOT EDIT.

package jsontomd

// slugStrip holds the code-point ranges github-slugger removes when slugging.
var slugStrip = [...][2]rune{
${lines.join("\n")}
}
`;

await mkdir(new URL("../go/", import.meta.url), { recursive: true });
await writeFile(new URL("../go/slugranges_gen.go", import.meta.url), src);
console.log(`wrote go/slugranges_gen.go: ${ranges.length} ranges, ${stripped.length} code points`);
